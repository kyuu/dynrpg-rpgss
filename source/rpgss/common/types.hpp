#ifndef RPGSS_TYPES_HPP_INCLUDED
#define RPGSS_TYPES_HPP_INCLUDED

#include <stdint.h>


namespace rpgss {

    typedef unsigned int uint;

    typedef int8_t  i8;
    typedef int16_t i16;
    typedef int32_t i32;
    typedef int64_t i64;

    typedef uint8_t  u8;
    typedef uint16_t u16;
    typedef uint32_t u32;
    typedef uint64_t u64;

    typedef float  f32;
    typedef double f64;

} // namespace rpgss


#endif // RPGSS_TYPES_HPP_INCLUDED
